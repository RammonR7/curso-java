package cursojava.basico;
/*
 * Classe demonstrativa de variáveis e constantes
 * 
 * versão 1.0
 */
public class VariaveisEConstantes {

	public static void main(String[] args) {
		// Criação de constantes.
		final double  Aceleracao_Gravidade_Terra = 9.806;
		
		// Criação de variáveis
		String nome = "Falção";
		
		int idade = 38;
		
		double peso = 74.6, altura = 1.80;
		
		char sexo = 'M', cnh = 'B';
		
		Boolean DoadorOrgaos = true;
		
			System.out.println("Nome: "+nome);
			System.out.println("");
			
			System.out.println("Idade: "+idade);
			System.out.println("");
			
			System.out.println("Peso: "+peso);
			System.out.println("");
			
			System.out.println("Altura: "+altura);
			System.out.println("");
			
			System.out.println("Sexo: "+sexo);
			System.out.println("");
			
			System.out.println("Habilitação: "+cnh);
			System.out.println("");
			
			System.out.println("Doador de órgãos: "+DoadorOrgaos);
			System.out.println("");
			
			System.out.println("Gravidade da terra: "+Aceleracao_Gravidade_Terra);
			System.out.println("");

	}

}
