package cursojava.basico;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		int n = 6, m = 9, p = 2, o = 5;
		System.out.println(n +" é maior que " +m+ " = " +(n>m));
		System.out.println(n +" é menor que " +m+ " = " +(p<o));
		System.out.println(n +" é maior ou igual " +m+ " = " +(p>=n));
		System.out.println(n +" é menor ou igual " +m+ " = " +(m<=o)); 
		
	}

}
